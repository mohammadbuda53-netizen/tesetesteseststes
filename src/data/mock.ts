export function getKundeName(kundeId: string): string {
  const kunde = kunden.find((k) => k.id === kundeId);
  if (!kunde) return "Unbekannt";
  return kunde.firma || `${kunde.vorname} ${kunde.nachname}`;
}

export function getHandwerkerName(handwerkerId: string): string {
  const hw = handwerker.find((h) => h.id === handwerkerId);
  if (!hw) return "Unbekannt";
  return `${hw.vorname} ${hw.nachname}`;
}
